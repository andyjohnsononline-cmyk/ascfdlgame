export default function CharacterPortrait({ character, expression = 'neutral', size = 'md' }) {
  const palette = PALETTES[character];
  const rows = PORTRAITS[character]?.[expression] || PORTRAITS[character]?.neutral;
  if (!palette || !rows) return null;

  const sizeMap = { sm: 3, md: 5, lg: 8, xl: 10, xxl: 14, env: 12 };
  const px = typeof size === 'number' ? size : (sizeMap[size] || PX);
  const cols = rows[0].length;
  const w = cols * px;
  const h = rows.length * px;

  const rects = [];
  rows.forEach((row, y) => {
    for (let x = 0; x < row.length; x++) {
      const ci = parseInt(row[x], 10);
      if (ci === 0) continue;
      const color = palette[ci];
      if (!color) continue;
      rects.push(
        <rect key={`${x}-${y}`} x={x * px} y={y * px} width={px} height={px} fill={color} />
      );
    }
  });

  return (
    <svg
      width={w}
      height={h}
      viewBox={`0 0 ${w} ${h}`}
      style={{ imageRendering: 'pixelated' }}
      aria-label={`${CHARACTER_NAMES[character]} portrait`}
    >
      {rects}
    </svg>
  );
}
